    <script>
        document.getElementById('contactForm').addEventListener('submit', function (e) {
            e.preventDefault();

            Swal.fire({
                title: 'Sending Message...',
                text: 'Please wait.',
                allowOutsideClick: false,
                didOpen: () => {
                    Swal.showLoading();
                }
            });

            fetch('submit.php', {
                method: 'POST',
                body: new FormData(this)
            })
                .then(response => response.json())
                .then(data => {
                    if (data.status === 'success') {
                        Swal.fire({
                            icon: 'success',
                            title: 'Sent!',
                            text: data.message,
                            confirmButtonColor: '#133a25'
                        }).then(() => {
                            document.getElementById('contactForm').reset();
                        });
                    } else {
                        Swal.fire({
                            icon: 'error',
                            title: 'Oops...',
                            text: data.message
                        });
                    }
                })
                .catch(error => {
                    Swal.fire({
                        icon: 'error',
                        title: 'System Error',
                        text: 'Something went wrong. Please try again later.'
                    });
                });
        });
    </script>
